    private void renderCountType(int option) {
        switch (option) {
            case 0:
                beginDate.setVisible(true);
                endDate.setVisible(true);
                month.setVisible(false);
                year.setVisible(false);
                break;
            case 1:
                month.setVisible(true);
                year.setVisible(true);
                beginDate.setVisible(false);
                endDate.setVisible(false);
                break;
            case 2:
                month.setVisible(false);
                year.setVisible(true);
                beginDate.setVisible(false);
                endDate.setVisible(false);     
                break;
            default:
                throw new AssertionError();
        }
    }
    
    private void renderTable(Date begin, Date end) {
        DefaultTableModel model = (DefaultTableModel) tbCount.getModel();
        model.setRowCount(0);
        dataset.clear();
        
        try {
            for (Object[] thongKe: KhachHangBLL.thongKeKhachHang(begin, end)) {
                model.addRow(thongKe);
                dataset.addValue((int)thongKe[4], "Lợi nhuận", (Comparable) thongKe[0]);
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    private void renderChart() {
        JFreeChart chart = ChartFactory.createBarChart(
            "Thống kê khách hàng",
            "Khách hàng",
            "Lợi nhuận",
            dataset,     
            PlotOrientation.HORIZONTAL, true, true, false
        );
        
        CategoryPlot plot = (CategoryPlot) chart.getPlot();
        BarRenderer renderer = (BarRenderer) plot.getRenderer();
        renderer.setMaximumBarWidth(0.2);
        
        ChartPanel chartPanel = new ChartPanel(chart);
        chartScrollPane.setViewportView(chartPanel);
    }

