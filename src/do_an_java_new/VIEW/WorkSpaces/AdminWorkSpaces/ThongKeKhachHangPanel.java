    private void btnCountMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCountMouseClicked
        // TODO add your handling code here:
        Date begin = null, end = null;
        int daysOfMonth;
        switch(cbbCountType.getSelectedIndex()) {
            case 0:
                begin = new Date(txtBeginDate.getDate().getTime());
                end = new Date(txtEndDate.getDate().getTime());
                break;
            case 1:
                begin = Date.valueOf(String.format("%d-%d-1", txtYear.getValue(), txtMonth.getMonth() + 1));
                daysOfMonth = LocalDate.of(txtYear.getValue(), txtMonth.getMonth(), 1).lengthOfMonth() - 1;
                end = Date.valueOf(String.format("%d-%d-%d", txtYear.getValue(), txtMonth.getMonth() + 1, daysOfMonth));
                break;
            case 2:
                begin = Date.valueOf(String.format("%d-1-1", txtYear.getValue()));
                end = Date.valueOf(String.format("%d-12-31", txtYear.getValue()));
                break;
            default:
                break;
        }
        
        renderTable(begin, end);
    }//GEN-LAST:event_btnCountMouseClicked


