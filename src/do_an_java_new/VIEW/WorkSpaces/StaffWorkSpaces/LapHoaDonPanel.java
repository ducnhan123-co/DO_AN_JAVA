    private void renderTable() {
        DefaultTableModel model = (DefaultTableModel) tbCTHD.getModel();
        model.setRowCount(0);
        
        try {
            for (ChiTietHoaDonDTO cthd: dsChiTietHoaDon) {
                model.addRow(new Object[]{
                    cthd.getMaHang(),
                    HangBLL.timHang(cthd.getMaHang()).getTenSP(),
                    cthd.getSoLuong(),
                    cthd.getDonGia(),
                    cthd.getDonGia()*cthd.getSoLuong()
                });
            }
        } catch(Exception e) {
            e.printStackTrace();
        }
        
        int tienGiam = tongTien*giamGia/100;
        
        txtTienGiam.setText(String.format("%d", tienGiam));
        txtTongTien.setText(String.format("%d", tongTien - tienGiam));
    }
    
    private void clearData() {
        dsChiTietHoaDon.clear();
        tongTien = 0;
        txtMaKhachHang.setText("");
        txtMaKM.setText("");
        renderTable();        
    }

