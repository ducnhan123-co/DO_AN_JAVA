    private void btnInsertMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInsertMouseClicked
        ChiTietHoaDonDTO cthd = new ChiTietHoaDonDTO();
        TimHangPopUp popUp = new TimHangPopUp(cthd);
        popUp.setVisible(true);
        
        popUp.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                super.windowClosed(e);
                if (cthd.getMaHang() == null)
                    return;
                for (ChiTietHoaDonDTO sub: dsChiTietHoaDon) 
                    if (sub.getMaHang().equals(cthd.getMaHang())) {
                        JOptionPane.showMessageDialog(null, "Hàng đã được thêm!", "", JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                dsChiTietHoaDon.add(cthd);
                tongTien += cthd.getDonGia() * cthd.getSoLuong();
                renderTable();
            }
        });
    }//GEN-LAST:event_btnInsertMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
        int selectedRow = tbCTHD.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Hãy chọn sản phẩm cần xoá");
            return;
        }
        
        for (int i = 0 ; i < dsChiTietHoaDon.size() ; i++) 
            if (dsChiTietHoaDon.get(i).getMaHang().equals(tbCTHD.getValueAt(selectedRow, 0))){
                tongTien -= dsChiTietHoaDon.get(i).getDonGia()*dsChiTietHoaDon.get(i).getSoLuong();
                dsChiTietHoaDon.remove(i);
                break;
            }
        
        renderTable();
    }//GEN-LAST:event_btnDeleteMouseClicked

