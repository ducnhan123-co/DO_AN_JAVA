    private void btnChangeAmountMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnChangeAmountMouseClicked
        // TODO add your handling code here:
        int selectedRow = tbCTHD.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Hãy chọn sản phẩm cần sửa");
            return;
        }
        
        for (int i = 0 ; i < dsChiTietHoaDon.size() ; i++) 
            if (dsChiTietHoaDon.get(i).getMaHang().equals(tbCTHD.getValueAt(selectedRow, 0))) {
                ChiTietHoaDonDTO ct = dsChiTietHoaDon.get(i);
                tongTien -= ct.getDonGia() * ct.getSoLuong();
                
                Wrapper tmp = new Wrapper(ct.getSoLuong());
                ChangeAmountPopUp popUp = new ChangeAmountPopUp(tmp); 
                
                popUp.setVisible(true);
                popUp.addWindowListener(new WindowAdapter() {
                    @Override
                    public void windowClosed(WindowEvent e) {
                        super.windowClosed(e); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/OverriddenMethodBody
                        ct.setSoLuong(tmp.getData());
                        tongTien += ct.getDonGia() * ct.getSoLuong();  
                        renderTable();
                    }
                });
                break;
            }
    }//GEN-LAST:event_btnChangeAmountMouseClicked

    private void btnSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSubmitMouseClicked
        // TODO add your handling code here:
        if (dsChiTietHoaDon.size() < 1) {
            JOptionPane.showMessageDialog(null, "Danh sách chi tiết hoá đơn đang bị trống, không thể tạo hoá đơn");
            return;
        }
        DateTimeFormatter timeFormatter = DateTimeFormatter.ofPattern("yymmddhhmmss");
        String 
                maHD = String.format("HD.%s.%s", maNhanVien, LocalDateTime.now().format(timeFormatter)),
                maKH = txtMaKhachHang.getText().trim();
        
        HoaDonDTO hoaDon = new HoaDonDTO(maHD, maKH, maNhanVien, tongTien, Date.valueOf(LocalDate.now()), txtMaKM.getText() == "" ? null : txtMaKM.getText());
        
        for (ChiTietHoaDonDTO ct: dsChiTietHoaDon) {
            ct.setMaHD(maHD);
        }
        
        try {
            HoaDonBLL.themHoaDon(hoaDon);
            
            for (ChiTietHoaDonDTO ct: dsChiTietHoaDon) 
                HangBLL.updateSoLuong(ct.getMaHang(), -ct.getSoLuong());
            
            KhuyenMaiBLL.updateSoLuong(hoaDon.getMaKM(), -1);
            
            ChiTietHoaDonBLL.themCTHD(dsChiTietHoaDon);
            
            int tienGiam = tongTien*giamGia/100;
            new BillPopUp(hoaDon, tienGiam).setVisible(true);
            
            clearData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Có lỗi", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSubmitMouseClicked

