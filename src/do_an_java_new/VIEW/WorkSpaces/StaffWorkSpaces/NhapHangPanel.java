    private void btnInsertMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInsertMouseClicked
        ChiTietNhapHangDTO ctnh = new ChiTietNhapHangDTO() ;
        HangDTO hang = new HangDTO() ;
        
        TimSanPhamPopUp popUp = new TimSanPhamPopUp(ctnh, hang);
        popUp.setVisible(true);
        
        popUp.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                super.windowClosed(e);
                if (hang.getMaHang() == null)
                    return;
                for (HangDTO sub: dsHang) 
                    if (sub.getMaSP().equals(hang.getMaSP())) {
                        JOptionPane.showMessageDialog(null, "Sản phẩm đã được thêm!", "", JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                
                dsChiTietNhapHang.add(ctnh);
                
                dsHang.add(hang);
                tongTien += ctnh.getDonGia() * ctnh.getSoLuong();
                renderTable();
            }
        });
    }//GEN-LAST:event_btnInsertMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
        int selectedRow = tbCTNH.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Hãy chọn sản phẩm cần xoá");
            return;
        }
        
        HangDTO hang = null;
        
        for (int i = 0 ; i < dsHang.size() ; i++) 
            if (dsHang.get(i).getMaSP().equals(tbCTNH.getValueAt(selectedRow, 0))) {
                hang = dsHang.get(i);
                dsHang.remove(i);
                break;
            }
        
        for (int i = 0 ; i < dsChiTietNhapHang.size() ; i++) {
            ChiTietNhapHangDTO ct = dsChiTietNhapHang.get(i);
            if (ct.getMaHang().equals(hang.getMaHang())) {
                tongTien -= ct.getDonGia() * ct.getSoLuong();
                dsChiTietNhapHang.remove(i);
            }
        }
        renderTable();
    }//GEN-LAST:event_btnDeleteMouseClicked

