    private void btnChangeAmountMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnChangeAmountMouseClicked
        // TODO add your handling code here:
        int selectedRow = tbCTNH.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Hãy chọn sản phẩm cần sửa");
            return;
        }
        
        for (HangDTO sub: dsHang) 
            if (sub.getMaSP().equals(tbCTNH.getValueAt(selectedRow, 0))) {
                HangDTO hang = sub;
                
                Wrapper tmp = new Wrapper(hang.getSoLuong());
                ChangeAmountPopUp popUp = new ChangeAmountPopUp(tmp);
                
                popUp.setVisible(true);
                popUp.addWindowListener(new WindowAdapter() {
                    @Override
                    public void windowClosed(WindowEvent e) {
                        super.windowClosed(e); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/OverriddenMethodBody

                        hang.setSoLuong(tmp.getData());
                        
                        for (ChiTietNhapHangDTO ct: dsChiTietNhapHang)
                            if (ct.getMaHang().equals(hang.getMaHang())) {
                                tongTien -= ct.getDonGia() * ct.getSoLuong();
                                ct.setSoLuong(tmp.getData());
                                tongTien += ct.getDonGia() * ct.getSoLuong();
                            }
                    renderTable();
                    }
                });
                break;
            }
        
    }//GEN-LAST:event_btnChangeAmountMouseClicked

    private void btnSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSubmitMouseClicked
        if (dsChiTietNhapHang.size() < 1 || dsHang.size() < 1) {
            JOptionPane.showMessageDialog(null, "Danh sách chi tiết nhập hàng đang bị trống, không thể tạo phiếu nhập");
            return;
        }
        DateTimeFormatter timeFormatter = DateTimeFormatter.ofPattern("yymmddhhmmss");
        String 
                maNH = String.format("NH.%s.%s", maNhanVien, LocalDateTime.now().format(timeFormatter)),
                maNCC = txtMaNCC.getText().trim();
        
        NhapHangDTO nh = new NhapHangDTO(maNH, maNCC, maNhanVien, tongTien, Date.valueOf(LocalDate.now()));
        for (ChiTietNhapHangDTO ct: dsChiTietNhapHang) 
            ct.setMaPhieu(maNH);
        
        try {
            
            HangBLL.themHang(dsHang);
            
            NhapHangBLL.themNhapHang(nh);
            ChiTietNhapHangBLL.themCTNH(dsChiTietNhapHang);
            
            JOptionPane.showMessageDialog(null, "Lập phiếu nhập hàng thành công");
            
            clearData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Có lỗi", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSubmitMouseClicked


