    private void renderTable() {
        DefaultTableModel model = (DefaultTableModel) tbCTNH.getModel();
        model.setRowCount(0);
        
        for (ChiTietNhapHangDTO ctnh: dsChiTietNhapHang) {
            HangDTO hang = null;
            for (HangDTO sub: dsHang)
                if (sub.getMaHang().equals(ctnh.getMaHang())) {
                    hang = sub;
                    break;
                }

            model.addRow(new Object[]{
                hang.getMaSP(),
                hang.getTenSP(),
                ctnh.getSoLuong(),
                ctnh.getDonGia(),
                ctnh.getDonGia()*ctnh.getSoLuong()
            });
        }
        
        txtTongTien.setText(String.format("%d", tongTien));
    }
    
    private void clearData() {
        dsChiTietNhapHang.clear();
        dsHang.clear();
        tongTien = 0;
        txtMaNCC.setText("");
        renderTable();        
    }

